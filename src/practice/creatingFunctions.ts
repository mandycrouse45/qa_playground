function fancyPrint(firstString: string, secondString: string): string {
    return `${firstString}\n ${secondString}`
}
console.log(fancyPrint("Bob", "Mandy"))

function funnyNames(firstName: string, secondName: string): string {
    return `${firstName}\n ${secondName}`
}
console.log(funnyNames("Mary", "Kalli"))